    private void deleteSongButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSongButtonActionPerformed
        String name;
        try{
            name=songList.getModel().getValueAt(songList.getSelectedRow(),0).toString();
            db.deleteSong(name);
        }catch(ArrayIndexOutOfBoundsException e)
        {
            JOptionPane.showMessageDialog(AllSongs.this, "No songs in the system!");
            return;
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(AllSongs.this, "Please try again!");
            return;
        }
        JOptionPane.showMessageDialog(AllSongs.this, "Successfully deleted from the system: "+name);
        new AllSongs().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_deleteSongButtonActionPerformed


    private void addplaylistButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addplaylistButtonActionPerformed
        String name="";
        String genre=songList.getModel().getValueAt(songList.getSelectedRow(),1).toString();
        String artist=songList.getModel().getValueAt(songList.getSelectedRow(),2).toString();
        String path=songList.getModel().getValueAt(songList.getSelectedRow(),3).toString();
        try{
            name=songList.getModel().getValueAt(songList.getSelectedRow(),0).toString();
            db.addSong(c.getUserName(),name,genre,artist,path);
        }catch(ArrayIndexOutOfBoundsException e)
        {
            JOptionPane.showMessageDialog(AllSongs.this, "No song is selected!");
            return;
        }
        catch(Exception s){
            JOptionPane.showMessageDialog(AllSongs.this, "You already have: "+name);
            return;
        }
        JOptionPane.showMessageDialog(AllSongs.this, "Successfully added to playlist: "+name);
        new AllSongs(c).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_addplaylistButtonActionPerformed

